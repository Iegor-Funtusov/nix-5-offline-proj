
public class TheBody {
    public String theBody(){
        return ("O never say that I was false of heart,\n" +
                "Though absence seemed my flame to qualify,\n" +
                "As easy might I from my self depart,\n" +
                "As from my soul which in thy breast doth lie:\n" +
                "That is my home of love, if I have ranged,\n" +
                "Like him that travels I return again,\n" +
                "Just to the time, not with the time exchanged,\n" +
                "So that my self bring water for my stain,\n" +
                "Never believe though in my nature reigned,\n" +
                "All frailties that besiege all kinds of blood,\n" +
                "That it could so preposterously be stained,\n" +
                "To leave for nothing all thy sum of good:\n" +
                "For nothing this wide universe I call,\n" +
                "Save thou my rose, in it thou art my all.");
    }
}