public class ChessFigure {
    private boolean chessColour;
    private int pown;
    private int rook;
    private int knight;
    private int bishop;
    private int queen;
    private int king;

    public boolean isChessColour() {
        return chessColour;
    }

    public boolean setChessColour(boolean chessColour) {
        this.chessColour = chessColour;
        return chessColour;
    }

    public int getPown() {
        return pown;
    }

    public int setPown(int pown) {
        this.pown = pown;
        return pown;
    }

    public int getRook() {
        return rook;
    }

    public int setRook(int rook) {
        this.rook = rook;
        return rook;
    }

    public int getKnight() {
        return knight;
    }

    public int setKnight(int knight) {
        this.knight = knight;
        return knight;
    }

    public int getBishop() {
        return bishop;
    }

    public int setBishop(int bishop) {
        this.bishop = bishop;
        return bishop;
    }

    public int getQueen() {
        return queen;
    }

    public int setQueen(int quin) {
        this.queen = queen;
        return queen;
    }

    public int getKing() {
        return king;
    }

    public int setKing(int king) {
        this.king = king;
        return king;
    }


}
