package com.lapchenko.kirill.project.lib;

public enum DateRegex {

    D_MM_YY("(([0-9])|([0-9][0-9]))/[0-9][0-9]/[0-9][0-9]"),

    M_YY_HH_MM_SS("/(([0-9])|([0-9][0-9]))/[0-9][0-9] [0-9][0-9]:[0-9][0-9]:[0-9][0-9]"),

    M("/(([0-9])|([0-9][0-9]))/"),

    YYYY_HH_MM("[0-9][0-9][0-9][0-9] [0-9][0-9]:[0-9][0-9]"),

    MMM_D_YY("([A-Za-z]+)-(([0-9])|([0-9][0-9]))-[0-9][0-9]"),

    DD_MMM_YYYY_HH_MM("[0-9][0-9]-([A-Za-z]+)-[0-9][0-9][0-9][0-9] [0-9][0-9]:[0-9][0-9]"),

    DD_MM_YY("[0-9][0-9]/[0-9][0-9]/[0-9][0-9]"),

    D_M_YYYY("(([0-9])|([0-9][0-9]))/(([0-9])|([0-9][0-9]))/[0-9][0-9][0-9][0-9]"),

    HH_MM("(0[0-9]|1[0-9]|2[0-3]):[0-5][0-9]");


    private DateRegex(String regex) {
        this.regex = regex;
    }

    private String regex;

    public String value() {
        return regex;
    }

}
